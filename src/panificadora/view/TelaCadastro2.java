    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
        salvarDadosF sd = new salvarDadosF();

        sd.setCodigo(CodigoF.getText());
        sd.setNome(NomeF.getText());
        sd.setCpf(CpfF.getText());
        sd.setUsuario(UserF.getText());
        sd.setSenha(String.valueOf(SenhaF.getPassword()));
        sd.setSobrenome(SobreF.getText());
        
        if(CodigoF.getText().equals("") || NomeF.getText().equals("") || CpfF.getText().equals("") || UserF.getText().equals("") || SenhaF.getPassword().equals("") ||  SobreF.getText().equals("")){
             JOptionPane.showMessageDialog(null,"Alguns campos não foram preenchidos","Aviso",JOptionPane.ERROR_MESSAGE);
        
        }else{
            JOptionPane.showMessageDialog(null, sd.SalvaDados());
        }

        CodigoF.setEnabled(true);
        CodigoF.setText("");
        NomeF.setText("");
        UserF.setText("");
        CpfF.setText("");
        SobreF.setText("");
        SenhaF.setText("");
    }//GEN-LAST:event_CadastrarActionPerformed

