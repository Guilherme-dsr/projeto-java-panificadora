    private void pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesquisarActionPerformed
        if(evt.getSource() == pesquisar){
            try{
                String codigo = JOptionPane.showInputDialog("Digite o Código:");

                BufferedReader ler = new BufferedReader(new FileReader("../Panificadora/Funcionarios/Funcionario"+codigo+".txt"));

                NomeF.setText(ler.readLine());
                CpfF.setText(ler.readLine());
                SobreF.setText(ler.readLine());
                UserF.setText(ler.readLine());
                SenhaF.setText(ler.readLine());
                CodigoF.setText(ler.readLine());

                ler.close();

            }catch(Exception erro){
                JOptionPane.showMessageDialog(null, "Erro: "+erro.getMessage());
            }
            NomeF.setEnabled(false);
            SobreF.setEnabled(false);
            CpfF.setEnabled(false);
            UserF.setEnabled(false);
            SenhaF.setEnabled(false);
            CodigoF.setEnabled(false);
        }
    }//GEN-LAST:event_pesquisarActionPerformed

